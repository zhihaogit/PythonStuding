-- 现代程序离不开关系数据库，要使用关系数据库就要掌握 SQL

-- NoSQL
-- 非 SQL的数据库，包括 MongoDB、Cassandra、Dynamo等
-- NoSQL发展历程
-- 1970: NoSQL = We have no SQL
-- 1980: NoSQL = Know SQL
-- 2000: NoSQL = No SQL!
-- 2005: NoSQL = Not only SQL
-- 2013: NoSQL = No, SQL!

-- SQL数据库仍然承担了各种应用程序的核心数据存储，而 NoSQL数据库作为 SQL数据库的补充，不是二选一关系，是主从关系